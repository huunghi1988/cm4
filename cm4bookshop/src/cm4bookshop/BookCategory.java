package cm4bookshop;

public class BookCategory {
private int id;
private Book book;
private Category category;
public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
public Book getBook() {
	return book;
}
public void setBook(Book book) {
	this.book = book;
}
public Category getCategory() {
	return category;
}
public void setCategory(Category category) {
	this.category = category;
}

}
